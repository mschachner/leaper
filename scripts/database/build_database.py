def read_jsonl(path):
    """Read a JSONL file, truncating a trailing corrupt line if present."""
    if not os.path.exists(path):
        return []
    entries = []
    with open(path) as f:
        lines = f.readlines()
    bad_tail = False
    for i, line in enumerate(lines):
        line = line.strip()
        if not line:
            continue
        try:
            entries.append(json.loads(line))
        except json.JSONDecodeError:
            if i == len(lines) - 1:
                bad_tail = True
            else:
                raise
    if bad_tail:
        with open(path, "w") as f:
            for e in entries:
                f.write(json.dumps(e) + "\n")
    return entries


def stage_prepare(workdir, geng, budget):
    os.makedirs(workdir, exist_ok=True)
    t0 = time.time()
    items, atlas = all_graphs(geng)
    entries_path = os.path.join(workdir, "entries.jsonl")
    gap_path = os.path.join(workdir, "gapin_all.txt")

    done = len(read_jsonl(entries_path))
    templates = named_templates()
    tindex = TemplateIndex(templates)
    aindex = AtlasIndex(atlas)

    ef = open(entries_path, "a")
    gf = open(gap_path, "a")

    for pos in range(done, len(items)):
        if time.time() - t0 > budget:
            ef.close()
            gf.close()
            print(f"PARTIAL {pos}/{len(items)}")
            return
        gid, atlas_no, G = items[pos]
        n = G.number_of_nodes()
        hops = enumerate_hops(G)
        props = compute_props(G)

        used = set()
        for h in hops:
            for u, v in enumerate(h):
                used.add((min(u, v), max(u, v)))
        edges_sorted = sorted([sorted(e) for e in G.edges()])
        redundant = [i for i, e in enumerate(edges_sorted)
                     if tuple(e) not in used]
        core = nx.Graph()
        core.add_nodes_from(range(n))
        redset = set(redundant)
        core.add_edges_from(e for i, e in enumerate(edges_sorted)
                            if i not in redset)
        leap_connected = nx.is_connected(core)

        name, aliases, family = name_graph(G, gid, tindex, aindex)
        layout = layout_graph(G, tindex)

        entry = {
            "id": gid,
            "atlas": atlas_no,
            "g6": nx.to_graph6_bytes(G, header=False).decode().strip(),
            "name": name,
            "aliases": aliases,
            "family": family,
            "n": n,
            "m": G.number_of_edges(),
            "edges": edges_sorted,
            "layout": [list(layout[v]) for v in range(n)],
            "hopCount": len(hops),
            "hasHop": len(hops) > 0,
            "redundant": redundant,
            "leapConnected": leap_connected,
            **{k: props[k] for k in
               ("connected", "components", "bipartite", "planar", "regular",
                "forest", "tree", "girth", "diameter", "triangles", "degrees")},
        }

        if hops:
            one_indexed = [[x + 1 for x in h] for h in hops]
            gf.write(f"{gid};{json.dumps(one_indexed)}\n")
            gf.flush()
        ef.write(json.dumps(entry) + "\n")

    ef.close()
    gf.close()
    print(f"DONE {len(items)} graphs")


GAP_DRIVER = """\
stream := InputTextFile("{infile}");;
out := OutputTextFile("{outfile}", false);;
SetPrintFormattingStatus(out, false);
line := ReadLine(stream);;
while line <> fail do
  line := Chomp(line);
  semi := Position(line, ';');
  id := line{{[1..semi-1]}};
  lists := EvalString(line{{[semi+1..Length(line)]}});
  gens := List(lists, PermList);
  G1 := Group(gens);
  s0 := gens[1];
  T := Concatenation(List(gens, s -> s * s0), List(gens, s -> s0^-1 * s));
  G2 := Group(T);
  AppendTo(out, id, ";", Order(G1), ";", StructureDescription(G1), ";",
    IsAbelian(G1), ";", Order(G2), ";", StructureDescription(G2), ";",
    IsAbelian(G2), "\\n");
  line := ReadLine(stream);
od;
CloseStream(out);
QUIT;
"""


def stage_gapsplit(workdir, n_chunks):
    jobs = {}
    with open(os.path.join(workdir, "gapin_all.txt")) as f:
        for line in f:
            line = line.rstrip("\n")
            if not line:
                continue
            gid = line.split(";", 1)[0]
            jobs[gid] = line  # keep last occurrence

    ordered = sorted(jobs.values(), key=len, reverse=True)
    chunks = [[] for _ in range(n_chunks)]
    loads = [0] * n_chunks
    for line in ordered:
        k = loads.index(min(loads))
        chunks[k].append(line)
        loads[k] += len(line)

    for k, chunk in enumerate(chunks):
        inpath = os.path.join(workdir, f"gapin_{k}.txt")
        outpath = os.path.join(workdir, f"gapout_{k}.txt")
        with open(inpath, "w") as f:
            f.write("\n".join(chunk) + "\n")
        with open(os.path.join(workdir, f"gaprun_{k}.g"), "w") as f:
            f.write(GAP_DRIVER.format(infile=inpath, outfile=outpath))
    print(f"split {len(jobs)} GAP jobs into {n_chunks} chunks")


def stage_merge(workdir, out_core, out_n8, n_chunks):
    raw = read_jsonl(os.path.join(workdir, "entries.jsonl"))
    entries = {}
    for e in raw:
        entries[e["id"]] = e  # keep last occurrence
    entries = list(entries.values())

    results = {}
    for k in range(n_chunks):
        p = os.path.join(workdir, f"gapout_{k}.txt")
        if not os.path.exists(p):
            continue
        with open(p) as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                gid, o1, d1, a1, o2, d2, a2 = line.split(";")
                results[gid] = {
                    "l1Order": int(o1), "l1": d1, "l1Abelian": a1 == "true",
                    "l2Order": int(o2), "l2": d2, "l2Abelian": a2 == "true",
                }

    missing = 0
    for e in entries:
        if e["hasHop"]:
            r = results.get(e["id"])
            if r is None:
                missing += 1
                continue
            e.update(r)
            e["reclusive"] = r["l1Order"] == r["l2Order"]
        else:
            e.update({"l1Order": 1, "l1": "1", "l1Abelian": True,
                      "l2Order": 1, "l2": "1", "l2Abelian": True,
                      "reclusive": False})

    if missing:
        raise SystemExit(f"ERROR: {missing} entries missing GAP results")

    core = [e for e in entries if e["n"] <= 7]
    n8 = [e for e in entries if e["n"] == 8]
    core.sort(key=lambda e: e["atlas"])
    n8.sort(key=lambda e: e["g6"])

    for path, data in ((out_core, core), (out_n8, n8)):
        with open(path, "w") as f:
            json.dump(data, f, separators=(",", ":"))
        print(f"wrote {len(data)} entries to {path} "
              f"({os.path.getsize(path)} bytes)")


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("stage", choices=["prepare", "gapsplit", "merge"])
    ap.add_argument("--workdir", required=True)
    ap.add_argument("--geng", default=DEFAULT_GENG)
    ap.add_argument("--budget", type=float, default=1e9,
                    help="prepare: stop cleanly after this many seconds")
    ap.add_argument("--chunks", type=int, default=40)
    ap.add_argument("--out-core", default="leapDatabaseCore.json")
    ap.add_argument("--out-n8", default="leapDatabaseN8.json")
    args = ap.parse_args()
    if args.stage == "prepare":
        stage_prepare(args.workdir, args.geng, args.budget)
    elif args.stage == "gapsplit":
        stage_gapsplit(args.workdir, args.chunks)
    else:
        stage_merge(args.workdir, args.out_core, args.out_n8, args.chunks)


if __name__ == "__main__":
    main()
