def enumerate_hops(G):
    """All hops on G as tuples p with p[i] = image of vertex i (0-indexed)."""
    n = G.number_of_nodes()
    adj = [sorted(G.neighbors(v)) for v in range(n)]
    res = []
    perm = [0] * n

    def bt(i, used):
        if i == n:
            res.append(tuple(perm))
            return
        for j in adj[i]:
            b = 1 << j
            if not used & b:
                perm[i] = j
                bt(i + 1, used | b)

    bt(0, 0)
    return res


