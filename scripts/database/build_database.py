def girth_of(G):
    if nx.is_forest(G):
        return None
    best = None
    for src in G.nodes:
        dist = {src: 0}
        parent = {src: None}
        queue = [src]
        while queue:
            u = queue.pop(0)
            for w in G.neighbors(u):
                if w not in dist:
                    dist[w] = dist[u] + 1
                    parent[w] = u
                    queue.append(w)
                elif parent[u] != w:
                    c = dist[u] + dist[w] + 1
                    if best is None or c < best:
                        best = c
    return best


def compute_props(G):
    n = G.number_of_nodes()
    degs = sorted((d for _, d in G.degree()), reverse=True)
    connected = nx.is_connected(G) if n > 0 else True
    tri = sum(nx.triangles(G).values()) // 3
    return {
        "connected": connected,
        "components": nx.number_connected_components(G),
        "bipartite": nx.is_bipartite(G),
        "planar": bool(nx.check_planarity(G)[0]),
        "regular": len(set(degs)) <= 1,
        "forest": nx.is_forest(G),
        "tree": nx.is_tree(G),
        "girth": girth_of(G),
        "diameter": nx.diameter(G) if connected and n > 0 else None,
        "triangles": tri,
        "degrees": degs,
    }


