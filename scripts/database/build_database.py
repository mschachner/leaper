def _circle(nodes, r=45.0, cx=0.0, cy=0.0, start=-math.pi / 2):
    k = len(nodes)
    return {
        v: (cx + r * math.cos(start + 2 * math.pi * i / k),
            cy + r * math.sin(start + 2 * math.pi * i / k))
        for i, v in enumerate(nodes)
    }


def _line(nodes, spacing=32.0):
    k = len(nodes)
    x0 = -spacing * (k - 1) / 2
    return {v: (x0 + spacing * i, 0.0) for i, v in enumerate(nodes)}


def _two_rows(top, bottom, spacing=36.0, gap=70.0):
    pos = {}
    for row, ys in ((top, -gap / 2), (bottom, gap / 2)):
        x0 = -spacing * (len(row) - 1) / 2
        for i, v in enumerate(row):
            pos[v] = (x0 + spacing * i, ys)
    return pos


def _from_edges(n, edges):
    G = nx.Graph()
    G.add_nodes_from(range(n))
    G.add_edges_from(edges)
    return G


