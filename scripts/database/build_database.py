def all_graphs(geng):
    """Yield (gid, atlas_number_or_None, G) for every simple graph, n=1..8."""
    items = []
    atlas = nx.graph_atlas_g()
    for i, G in enumerate(atlas):
        if G.number_of_nodes() == 0:
            continue
        items.append((f"G{i}", i, G))
    out = subprocess.run([geng, "-q", "8"], capture_output=True, text=True,
                         check=True)
    for line in out.stdout.split():
        G = nx.from_graph6_bytes(line.encode())
        items.append((line, None, G))
    return items, atlas


