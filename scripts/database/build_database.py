#!/usr/bin/env python3
"""Build the Leaper leap-group database (all simple graphs on 1..8 vertices).

Sources: the networkx Graph Atlas for n <= 7 (Read & Wilson numbering, ids
G1..G1252) and nauty's geng for n = 8 (ids are canonical graph6 strings).

Stages (in order):
    prepare   Enumerate graphs; compute hops, leap-redundant edges,
              properties, names, layouts. Resumable: appends to
              WORK/entries.jsonl + WORK/gapin_all.txt and stops cleanly
              after --budget seconds; rerun until it prints DONE.
    gapsplit  Split GAP input into WORK/gapin_k.txt + driver WORK/gaprun_k.g.
    <run GAP on each driver:  gap -q -A -l GAPROOT WORK/gaprun_k.g >
    merge     Combine entries + GAP output; write the core (n<=7) and n=8
              datasets separately so the frontend can lazy-load them.

Lambda_2 is computed inside GAP as the subgroup generated by
{ s*s0 } union { s0^-1*s } over all hops s, for a fixed hop s0: every
even-length word in hops is a product of such pairs.
"""

