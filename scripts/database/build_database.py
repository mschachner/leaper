def graph_key(G):
    return (G.number_of_nodes(), G.number_of_edges(),
            tuple(sorted(d for _, d in G.degree())))


class TemplateIndex:
    def __init__(self, templates):
        self.buckets = {}
        for t in templates:
            self.buckets.setdefault(t["key"], []).append(t)

    def match(self, G):
        """Return (template, mapping G_node -> template_node) or (None, None)."""
        for t in self.buckets.get(graph_key(G), []):
            gm = nx.algorithms.isomorphism.GraphMatcher(G, t["G"])
            if gm.is_isomorphic():
                return t, gm.mapping
        return None, None


class AtlasIndex:
    """Bucketed lookup: connected component (n<=7) -> atlas number."""

    def __init__(self, atlas):
        self.buckets = {}
        for i, G in enumerate(atlas):
            if G.number_of_nodes() == 0:
                continue
            self.buckets.setdefault(graph_key(G), []).append((i, G))

    def index_of(self, H):
        for i, G in self.buckets.get(graph_key(H), []):
            if nx.is_isomorphic(H, G):
                return i
        return None


