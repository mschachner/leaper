def layout_component(H, tindex):
    t, mapping = tindex.match(H)
    if t is not None and t["pos"] is not None:
        return {v: t["pos"][mapping[v]] for v in H.nodes}
    if H.number_of_nodes() == 1:
        return {next(iter(H.nodes)): (0.0, 0.0)}
    if H.number_of_nodes() <= 7:
        pos = nx.kamada_kawai_layout(H)
    else:
        pos = nx.spring_layout(H, seed=42, iterations=100)
    return {v: (45.0 * x, 45.0 * y) for v, (x, y) in pos.items()}


def layout_graph(G, tindex):
    comps = [G.subgraph(c).copy() for c in
             sorted(nx.connected_components(G), key=lambda c: (-len(c), sorted(c)))]
    placed = {}
    x_cursor = 0.0
    gap = 40.0
    for H in comps:
        pos = layout_component(H, tindex)
        xs = [p[0] for p in pos.values()]
        ys = [p[1] for p in pos.values()]
        minx, maxx = min(xs), max(xs)
        miny, maxy = min(ys), max(ys)
        for v, (x, y) in pos.items():
            placed[v] = (x_cursor + (x - minx), (y - (miny + maxy) / 2))
        x_cursor += (maxx - minx) + gap
    return {v: (round(x), round(y)) for v, (x, y) in placed.items()}


