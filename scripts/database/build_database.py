def name_graph(G, gid, tindex, aindex):
    """Return (name, aliases, family)."""
    t, _ = tindex.match(G)
    if t is not None:
        return t["name"], list(t["aliases"]), t["family"]

    if not nx.is_connected(G):
        parts = []
        for c in sorted(nx.connected_components(G), key=lambda c: (-len(c), sorted(c))):
            H = nx.convert_node_labels_to_integers(
                G.subgraph(c), ordering="sorted")
            tc, _ = tindex.match(H)
            if tc is not None:
                parts.append(tc["name"])
            else:
                idx = aindex.index_of(H)
                parts.append(f"G{idx}" if idx is not None else "?")
        return " + ".join(parts), ["disjoint union"], "Disjoint union"

    if nx.is_tree(G):
        return gid, [], "Tree"
    return gid, [], "Other"


